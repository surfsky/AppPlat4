///     
/// 角色
///     角色是权限的集合
///     角色和权限的关系可在后台进行配置
///     角色可以动态增减，可在后台进行配置
///     
/// 用户
///     拥有多个角色
///     系统可根据用户拥有的角色对应的权限列表，来进行授权操作
///     注意：不推荐直接根据角色来授权，会导致逻辑不清晰
/// </summary>
