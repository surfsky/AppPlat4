/// <summary>
/// 用户-角色-权限 三级授权机制
/// 权限
///     系统只根据权限来限制操作，如修改按钮是否可用，删除按钮是否可用等
///     编写页面时无需考虑角色
///     权限是可以预料且内置的，如产品查看、产品修改、产品新建、产品删除权限
